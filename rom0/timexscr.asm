	SRL	D
	RRA
	RRA
	RRA
	RRA
	LD	L,A
	LD	A,C
	RRA
	SCF
	RRA
	AND	A
	RRA
	XOR	C
	AND	$F8
	XOR	C
	LD	H,A
	LD	A,C
	ADD	A,A
	ADD	A,A
	XOR	L
	AND	$E0
	XOR	L
	LD	L,A
	LD	A,E
	AND	$07
	RET

