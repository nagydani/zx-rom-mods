DPOKE:	RST	$30
	DEFW	L1E99	; FIND-INT2
	PUSH	BC	; value
	RST	$30
	DEFW	L1E99	; FIND-INT2
	LD	L,C
	LD	H,B	; HL = address
	POP	BC
	LD	(HL),C
	INC	HL
	LD	(HL),B
	RST	$10

FPOKE:	RST	$28
	DEFB	$01	; exchange
	DEFB	$38	; end
	RST	$30
	DEFW	L1E99	; FIND-INT2
	PUSH	BC	; address
	CALL	L2BF1	; STK-FETCH (no need for RST $30)
	POP	DE
	LD	BC,5
POKE_L:	LDIR
	RST	$10

E_POKE:	POP	HL	; discard REPORT-C
	CALL	CHECK_END
	CALL	L2BF1	; STK-FETCH
	PUSH	DE
	PUSH	BC
	RST	$30
	DEFW	L1E99	; FIND-INT2
	LD	E,C
	LD	D,B
	POP	BC
	POP	HL
	LD	A,B
	OR	C
	JR	NZ,POKE_L
	RST	$10
